// 在这里写代码……

function clone(obj) {
  // 使用ES6的扩展运算符进行深拷贝
  var { ...obj2 } = obj;
  return obj2;
}

function clone2(obj) {
  // 使用assign进行深拷贝
  return Object.assign({}, obj);
}

// * ---------------- 实现的效果：

{
  const data = {
    person: [
      {
        id: 114514,
        age: 24,
        type: 'student',
      },
    ],
  };

  const mirrorData = clone2(data);

  mirrorData.person[0].age = 19;

  console.log(mirrorData.person[0].age === 19);
  console.log(data.person[0].age === 24);
}
